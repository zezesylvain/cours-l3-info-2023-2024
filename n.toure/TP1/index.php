	<p > <strong>La programmation</strong>, appelée aussi codage dans le domaine informatique1, 
		désigne l'ensemble des activités qui permettent l'écriture des programmes 
		informatiques. C'est une étape importante du développement de logiciels.
		L'écriture d'un programme se fait dans un langage de programmation.
		 Un logiciel est un ensemble de programmes (qui peuvent être écrits dans
		  des langages de programmation différents) dédié à la réalisation de 
		  certaines tâches par un (ou plusieurs) utilisateurs du logiciel.
		<br>
		La programmation représente donc ici la rédaction du code source d'un 
		logiciel. On utilise plutôt le terme développement pour dénoter l'ensemble
		 des activités liées à la création d'un logiciel et des programmes qui le
		  composent. Cela inclut la spécification du logiciel, sa conception, 
		  puis son implémentation proprement dite au sens de l'écriture des 
		  programmes dans un langage de programmation bien défini, ainsi que
		   la vérification de sa correction, etc.
